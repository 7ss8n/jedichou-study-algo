// 4.6 整型概述

#include <iostream>
using namespace std;
int main()
{
    cout << "bool:" << sizeof(bool) << endl;
    cout << "int:" << sizeof(int) << endl;
    cout << "short:" << sizeof(short) << endl;
    cout << "long:" << sizeof(long) << endl;
    cout << "char:" << sizeof(char) << endl;
    cout << "float:" << sizeof(float) << endl;
    cout << "double:" << sizeof(double) << endl;
    return 0;
}